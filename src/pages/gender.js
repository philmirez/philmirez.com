const Timer = ({ n }) => (
  <div className={'timer'}>
    {new Date(n * 1000).toISOString().substr(11, 8)}
  </div>
)

const Flicker = ({ n }) => {
  const [currentN, setN] = useState(n)

  useEffect(() => {
    if (currentN === 1) {
      return
    }
    setTimeout(() => {
      setN(currentN - 1)
    },1000)
  }, [currentN])

  return (
    <>
      <Timer n={currentN} />
      {(currentN % 2 === 0) ? <Girl /> : <Boy />}
    </>
  )
}

const GenderPage = () => (
    <Flicker n={1800} />
)

export default GenderPage